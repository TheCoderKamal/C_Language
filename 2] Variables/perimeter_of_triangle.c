// Q.3 Write a Program to find the perimeter of a triangle.

#include<stdio.h>
int main(){
    int side_1 = 12;
    int side_2 = 12;
    int base = 23;

    int perimeter_of_triangle = side_1 + side_2 + base;

    printf("The perimeter of the triangle : %d units", perimeter_of_triangle);
    return 0;
}